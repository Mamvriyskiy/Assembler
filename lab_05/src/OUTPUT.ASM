EXTRN TO_SHEX : NEAR
EXTRN TO_UBIN : NEAR
EXTRN SHEX : BYTE
EXTRN UBIN : BYTE
EXTRN SIGN : BYTE

PUBLIC NEWLINE
PUBLIC OUTPUT_SIGNED_HEX
PUBLIC OUTPUT_UNSIGNED_BIN


CODESEG SEGMENT PARA PUBLIC 'CODE'
    ASSUME CS:CODESEG

NEWLINE PROC NEAR
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
    RET
NEWLINE ENDP

OUTPUT_SIGNED_HEX PROC NEAR

    CALL TO_SHEX

    MOV AH, 2
    MOV DL, SIGN
    INT 21H

    MOV AH, 9
    MOV DX, OFFSET SHEX
    INT 21H

    CALL NEWLINE
    CALL NEWLINE

    RET
OUTPUT_SIGNED_HEX ENDP

OUTPUT_UNSIGNED_BIN PROC NEAR

    CALL TO_UBIN

    MOV AH, 9
    MOV DX, OFFSET UBIN
    INT 21H

    CALL NEWLINE
    CALL NEWLINE

    RET
OUTPUT_UNSIGNED_BIN ENDP

CODESEG ENDS
END